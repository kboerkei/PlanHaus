// Get current locale (default to 'en')
const getCurrentLocale = (): Locale => {
  // In a real app, this would come from user preferences, URL params, or browser settings
  return 'en';
};

// Get nested translation value
const getNestedValue = (obj: any, path: string): string => {
  return path.split('.').reduce((current, key) => current?.[key], obj) || path;
};

// Main translation function
export const t = (key: string, locale?: Locale): string => {
  const currentLocale = locale || getCurrentLocale();
  const translation = translations[currentLocale];
  
  if (!translation) {
    console.warn(`Translation not found for locale: ${currentLocale}`);
    return key;
  }
  
  return getNestedValue(translation, key);
};

// Hook for reactive translations
export const useTranslation = (locale?: Locale) => {
  return {
    t: (key: string) => t(key, locale),
    locale: locale || getCurrentLocale(),
  };
};

// Export types
export type { Locale, Translations }; 